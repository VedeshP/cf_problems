int gcd(int a,int b)
{
    if(b == 0) return a;
    else return gcd(b, a % b);
}

int lcm(int a,int b)
{
    return a * b / gcd(a, b);
}

bool is_prime(ll n)
{
    for (ll i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
        {
            return false;
        }
    }

    return true;
}

void solve()
{
    ll d; cin >> d;

    // if N = p1 ^ a * p2 ^ b * p3 ^ c
    // then number of divisors of N = (a + 1) * (b + 1) * (c + 1)


    ll x, y;
    x = 1 + d;
    while (!is_prime(x)) ++x;
    y = x + d;
    // cout << 'f' << '\n';
    while (!is_prime(y)) ++y;

    cout << min((1LL * x * y), (1LL * x*x*x)) << '\n';
}

int main(void)
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    
    int t;
    cin >> t;
    for (int i = 0; i < t; i++)
    {
        solve();
    }
}
